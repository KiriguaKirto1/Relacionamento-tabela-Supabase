-- ============================================================
-- TÍTULO: Sistema de Pedidos com Clientes, Produtos e Itens
-- DESCRIÇÃO: Atividade SQL para cadastrar clientes, produtos,
-- pedidos e itens de pedido, usando chaves primárias, estrangeiras,
-- validações CHECK e consultas com JOIN.
-- BANCO: Supabase / PostgreSQL
-- ============================================================

DROP TABLE IF EXISTS itens_pedido;
DROP TABLE IF EXISTS pedidos;
DROP TABLE IF EXISTS produtos;
DROP TABLE IF EXISTS clientes;

CREATE TABLE clientes (
    id_cliente BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cpf VARCHAR(11) NOT NULL UNIQUE,
    nome VARCHAR(100) NOT NULL,
    telefone VARCHAR(11),
    email VARCHAR(100),
    cidade VARCHAR(100),
    estado CHAR(2),

    CHECK (cpf ~ '^[0-9]{11}$'),
    CHECK (telefone IS NULL OR telefone ~ '^[0-9]{11}$'),
    CHECK (estado IS NULL OR estado ~ '^[A-Z]{2}$'),
    CHECK (email IS NULL OR email LIKE '%@%')
);

CREATE TABLE produtos (
    id_produto BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    produto VARCHAR(100) NOT NULL,
    categoria VARCHAR(50),
    preco_unitario NUMERIC(10,2) NOT NULL CHECK (preco_unitario >= 0),
    quantidade_disponivel INTEGER NOT NULL CHECK (quantidade_disponivel >= 0)
);

CREATE TABLE pedidos (
    id_pedido BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente BIGINT NOT NULL,
    data_pedido DATE NOT NULL,
    status_pedido VARCHAR(20) NOT NULL CHECK (
        status_pedido IN ('Aberto', 'Finalizado', 'Cancelado')
    ),

    FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente)
);

CREATE TABLE itens_pedido (
    id_item BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_pedido BIGINT NOT NULL,
    id_produto BIGINT NOT NULL,
    quantidade INTEGER NOT NULL CHECK (quantidade > 0),

    FOREIGN KEY (id_pedido) REFERENCES pedidos(id_pedido),
    FOREIGN KEY (id_produto) REFERENCES produtos(id_produto)
);

