-- Consulta 1: clientes com CPF e telefone formatados.
SELECT
    id_cliente,
    SUBSTR(cpf, 1, 3) || '.' ||
    SUBSTR(cpf, 4, 3) || '.' ||
    SUBSTR(cpf, 7, 3) || '-' ||
    SUBSTR(cpf, 10, 2) AS cpf_formatado,
    nome,
    '(' || SUBSTR(telefone, 1, 2) || ') ' ||
    SUBSTR(telefone, 3, 5) || '-' ||
    SUBSTR(telefone, 8, 4) AS telefone_formatado,
    email,
    cidade,
    estado
FROM clientes;

-- Consulta 2: produtos com preço formatado.
SELECT
    id_produto,
    produto,
    categoria,
    'R$ ' || REPLACE(TO_CHAR(preco_unitario, 'FM999999990.00'), '.', ',') AS preco,
    quantidade_disponivel
FROM produtos;

-- Consulta 3: pedidos com data formatada.
SELECT
    id_pedido,
    id_cliente,
    TO_CHAR(data_pedido, 'DD/MM/YYYY') AS data_pedido,
    status_pedido
FROM pedidos;

-- Consulta 4: relatório completo dos pedidos.
SELECT
    pedidos.id_pedido AS pedido,
    TO_CHAR(pedidos.data_pedido, 'DD/MM/YYYY') AS data_pedido,
    SUBSTR(clientes.cpf, 1, 3) || '.' ||
    SUBSTR(clientes.cpf, 4, 3) || '.' ||
    SUBSTR(clientes.cpf, 7, 3) || '-' ||
    SUBSTR(clientes.cpf, 10, 2) AS cpf,
    clientes.nome AS cliente,
    '(' || SUBSTR(clientes.telefone, 1, 2) || ') ' ||
    SUBSTR(clientes.telefone, 3, 5) || '-' ||
    SUBSTR(clientes.telefone, 8, 4) AS telefone,
    clientes.cidade,
    clientes.estado,
    produtos.produto,
    produtos.categoria,
    itens_pedido.quantidade,
    'R$ ' || REPLACE(TO_CHAR(produtos.preco_unitario, 'FM999999990.00'), '.', ',') AS preco_unitario,
    'R$ ' || REPLACE(
        TO_CHAR(itens_pedido.quantidade * produtos.preco_unitario, 'FM999999990.00'),
        '.',
        ','
    ) AS valor_total,
    pedidos.status_pedido
FROM itens_pedido
INNER JOIN pedidos
    ON itens_pedido.id_pedido = pedidos.id_pedido
INNER JOIN clientes
    ON pedidos.id_cliente = clientes.id_cliente
INNER JOIN produtos
    ON itens_pedido.id_produto = produtos.id_produto;
