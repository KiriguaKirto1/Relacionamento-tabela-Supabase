DROP TABLE IF EXISTS pagamentos;
DROP TABLE IF EXISTS prontuarios;
DROP TABLE IF EXISTS consultas;
DROP TABLE IF EXISTS horarios_medicos;
DROP TABLE IF EXISTS salas;
DROP TABLE IF EXISTS tipos_atendimento;
DROP TABLE IF EXISTS medico_especialidades;
DROP TABLE IF EXISTS especialidades;
DROP TABLE IF EXISTS paciente_convenios;
DROP TABLE IF EXISTS convenios;
DROP TABLE IF EXISTS medicos;
DROP TABLE IF EXISTS pacientes;

CREATE TABLE pacientes (
    id_paciente BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cpf VARCHAR(11) NOT NULL UNIQUE,
    nome VARCHAR(100) NOT NULL,
    data_nascimento DATE NOT NULL,
    sexo VARCHAR(20) NOT NULL DEFAULT 'Nao informado'
        CHECK (sexo IN ('Masculino', 'Feminino', 'Outro', 'Nao informado')),
    telefone VARCHAR(11),
    email VARCHAR(100),
    endereco TEXT,
    cidade VARCHAR(100),
    estado CHAR(2),
    nome_responsavel VARCHAR(100),
    telefone_emergencia VARCHAR(11),
    alergias TEXT,
    observacoes TEXT,
    status_paciente VARCHAR(20) NOT NULL CHECK (status_paciente IN ('Ativo', 'Inativo')),

    CHECK (cpf ~ '^[0-9]{11}$'),
    CHECK (cpf NOT IN (
        '00000000000','11111111111','22222222222','33333333333','44444444444',
        '55555555555','66666666666','77777777777','88888888888','99999999999'
    )),
    CHECK (telefone IS NULL OR telefone ~ '^[0-9]{10,11}$'),
    CHECK (telefone_emergencia IS NULL OR telefone_emergencia ~ '^[0-9]{10,11}$'),
    CHECK (email IS NULL OR email LIKE '%@%.%'),
    CHECK (estado IS NULL OR estado ~ '^[A-Z]{2}$')
);

CREATE TABLE medicos (
    id_medico BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    crm VARCHAR(20) NOT NULL,
    estado_crm CHAR(2) NOT NULL CHECK (estado_crm ~ '^[A-Z]{2}$'),
    nome VARCHAR(100) NOT NULL,
    telefone VARCHAR(11),
    email VARCHAR(100),
    status_medico VARCHAR(20) NOT NULL CHECK (status_medico IN ('Ativo', 'Inativo')),

    UNIQUE (crm, estado_crm),
    CHECK (telefone IS NULL OR telefone ~ '^[0-9]{10,11}$'),
    CHECK (email IS NULL OR email LIKE '%@%.%')
);

CREATE TABLE convenios (
    id_convenio BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_convenio VARCHAR(100) NOT NULL UNIQUE,
    telefone VARCHAR(11),
    status_convenio VARCHAR(20) NOT NULL CHECK (status_convenio IN ('Ativo', 'Inativo')),

    CHECK (telefone IS NULL OR telefone ~ '^[0-9]{10,11}$')
);

CREATE TABLE paciente_convenios (
    id_paciente BIGINT NOT NULL,
    id_convenio BIGINT NOT NULL,
    numero_carteirinha VARCHAR(50) NOT NULL,
    status_paciente_convenio VARCHAR(20) NOT NULL CHECK (
        status_paciente_convenio IN ('Ativo', 'Inativo')
    ),

    PRIMARY KEY (id_paciente, id_convenio),
    UNIQUE (id_convenio, numero_carteirinha),

    FOREIGN KEY (id_paciente) REFERENCES pacientes(id_paciente) ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY (id_convenio) REFERENCES convenios(id_convenio) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE especialidades (
    id_especialidade BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_especialidade VARCHAR(100) NOT NULL UNIQUE,
    descricao TEXT,
    status_especialidade VARCHAR(20) NOT NULL CHECK (status_especialidade IN ('Ativo', 'Inativo'))
);

CREATE TABLE medico_especialidades (
    id_medico BIGINT NOT NULL,
    id_especialidade BIGINT NOT NULL,

    PRIMARY KEY (id_medico, id_especialidade),

    FOREIGN KEY (id_medico) REFERENCES medicos(id_medico) ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY (id_especialidade) REFERENCES especialidades(id_especialidade) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE tipos_atendimento (
    id_tipo_atendimento BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_especialidade BIGINT NOT NULL,
    nome_atendimento VARCHAR(100) NOT NULL,
    descricao TEXT,
    status_tipo VARCHAR(20) NOT NULL CHECK (status_tipo IN ('Ativo', 'Inativo')),

    UNIQUE (id_tipo_atendimento, id_especialidade),
    UNIQUE (id_especialidade, nome_atendimento),

    FOREIGN KEY (id_especialidade) REFERENCES especialidades(id_especialidade) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE salas (
    id_sala BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_sala VARCHAR(50) NOT NULL UNIQUE,
    status_sala VARCHAR(20) NOT NULL CHECK (status_sala IN ('Ativa', 'Inativa'))
);

CREATE TABLE horarios_medicos (
    id_horario BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_medico BIGINT NOT NULL,
    dia_semana INTEGER NOT NULL CHECK (dia_semana BETWEEN 0 AND 6),
    hora_inicio TIME NOT NULL,
    hora_fim TIME NOT NULL,
    status_horario VARCHAR(20) NOT NULL CHECK (status_horario IN ('Ativo', 'Inativo')),

    FOREIGN KEY (id_medico) REFERENCES medicos(id_medico) ON UPDATE CASCADE ON DELETE RESTRICT,
    CHECK (hora_fim > hora_inicio)
);

CREATE TABLE consultas (
    id_consulta BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_paciente BIGINT NOT NULL,
    id_medico BIGINT NOT NULL,
    id_especialidade BIGINT NOT NULL,
    id_tipo_atendimento BIGINT NOT NULL,
    id_sala BIGINT NOT NULL,
    forma_atendimento VARCHAR(20) NOT NULL CHECK (forma_atendimento IN ('Particular', 'Convenio')),
    id_convenio BIGINT,
    data_agendamento DATE NOT NULL,
    data_consulta DATE NOT NULL,
    hora_inicio TIME NOT NULL,
    hora_fim TIME NOT NULL,
    valor_consulta_centavos INTEGER NOT NULL CHECK (valor_consulta_centavos >= 0),
    status_consulta VARCHAR(20) NOT NULL CHECK (status_consulta IN ('Agendada', 'Realizada', 'Cancelada')),
    motivo_cancelamento TEXT,
    observacoes TEXT,

    FOREIGN KEY (id_paciente) REFERENCES pacientes(id_paciente) ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY (id_sala) REFERENCES salas(id_sala) ON UPDATE CASCADE ON DELETE RESTRICT,

    FOREIGN KEY (id_medico, id_especialidade)
        REFERENCES medico_especialidades(id_medico, id_especialidade)
        ON UPDATE CASCADE ON DELETE RESTRICT,

    FOREIGN KEY (id_tipo_atendimento, id_especialidade)
        REFERENCES tipos_atendimento(id_tipo_atendimento, id_especialidade)
        ON UPDATE CASCADE ON DELETE RESTRICT,

    FOREIGN KEY (id_paciente, id_convenio)
        REFERENCES paciente_convenios(id_paciente, id_convenio)
        ON UPDATE CASCADE ON DELETE RESTRICT,

    CHECK (
        (forma_atendimento = 'Particular' AND id_convenio IS NULL)
        OR
        (forma_atendimento = 'Convenio' AND id_convenio IS NOT NULL)
    ),
    CHECK (
        (status_consulta <> 'Cancelada' AND motivo_cancelamento IS NULL)
        OR
        (status_consulta = 'Cancelada' AND motivo_cancelamento IS NOT NULL)
    ),
    CHECK (data_consulta >= data_agendamento),
    CHECK (hora_fim > hora_inicio)
);

CREATE TABLE prontuarios (
    id_prontuario BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_consulta BIGINT NOT NULL UNIQUE,
    queixa_principal TEXT NOT NULL,
    diagnostico TEXT,
    prescricao TEXT,
    observacoes_clinicas TEXT,

    FOREIGN KEY (id_consulta) REFERENCES consultas(id_consulta) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE pagamentos (
    id_pagamento BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_consulta BIGINT NOT NULL,
    forma_pagamento VARCHAR(20) NOT NULL CHECK (forma_pagamento IN ('Dinheiro', 'Cartao', 'Pix', 'Convenio')),
    valor_pago_centavos INTEGER NOT NULL CHECK (valor_pago_centavos >= 0),
    status_pagamento VARCHAR(20) NOT NULL CHECK (status_pagamento IN ('Pendente', 'Pago', 'Cancelado')),

    FOREIGN KEY (id_consulta) REFERENCES consultas(id_consulta) ON UPDATE CASCADE ON DELETE RESTRICT,
    UNIQUE (id_consulta, forma_pagamento)
);

CREATE OR REPLACE FUNCTION validar_consulta()
RETURNS TRIGGER AS $$
BEGIN
    IF (SELECT status_paciente FROM pacientes WHERE id_paciente = NEW.id_paciente) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: paciente inativo';
    END IF;

    IF (SELECT status_medico FROM medicos WHERE id_medico = NEW.id_medico) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: medico inativo';
    END IF;

    IF (SELECT status_especialidade FROM especialidades WHERE id_especialidade = NEW.id_especialidade) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: especialidade inativa';
    END IF;

    IF (SELECT status_tipo FROM tipos_atendimento WHERE id_tipo_atendimento = NEW.id_tipo_atendimento) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: tipo de atendimento inativo';
    END IF;

    IF (SELECT status_sala FROM salas WHERE id_sala = NEW.id_sala) <> 'Ativa' THEN
        RAISE EXCEPTION 'Erro: sala inativa';
    END IF;

    IF NEW.forma_atendimento = 'Convenio'
       AND (SELECT status_convenio FROM convenios WHERE id_convenio = NEW.id_convenio) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: convenio inativo';
    END IF;

    IF NEW.forma_atendimento = 'Convenio'
       AND (
            SELECT status_paciente_convenio
            FROM paciente_convenios
            WHERE id_paciente = NEW.id_paciente
              AND id_convenio = NEW.id_convenio
       ) <> 'Ativo' THEN
        RAISE EXCEPTION 'Erro: convenio do paciente inativo';
    END IF;

    IF NEW.status_consulta <> 'Cancelada'
       AND NOT EXISTS (
            SELECT 1
            FROM horarios_medicos
            WHERE id_medico = NEW.id_medico
              AND dia_semana = EXTRACT(DOW FROM NEW.data_consulta)::INTEGER
              AND status_horario = 'Ativo'
              AND hora_inicio <= NEW.hora_inicio
              AND hora_fim >= NEW.hora_fim
       ) THEN
        RAISE EXCEPTION 'Erro: consulta fora do horario de atendimento do medico';
    END IF;

    IF NEW.status_consulta <> 'Cancelada'
       AND EXISTS (
            SELECT 1
            FROM consultas
            WHERE id_medico = NEW.id_medico
              AND data_consulta = NEW.data_consulta
              AND status_consulta <> 'Cancelada'
              AND NEW.hora_inicio < hora_fim
              AND NEW.hora_fim > hora_inicio
              AND (TG_OP = 'INSERT' OR id_consulta <> NEW.id_consulta)
       ) THEN
        RAISE EXCEPTION 'Erro: conflito de horario para o mesmo medico';
    END IF;

    IF NEW.status_consulta <> 'Cancelada'
       AND EXISTS (
            SELECT 1
            FROM consultas
            WHERE id_paciente = NEW.id_paciente
              AND data_consulta = NEW.data_consulta
              AND status_consulta <> 'Cancelada'
              AND NEW.hora_inicio < hora_fim
              AND NEW.hora_fim > hora_inicio
              AND (TG_OP = 'INSERT' OR id_consulta <> NEW.id_consulta)
       ) THEN
        RAISE EXCEPTION 'Erro: conflito de horario para o paciente';
    END IF;

    IF NEW.status_consulta <> 'Cancelada'
       AND EXISTS (
            SELECT 1
            FROM consultas
            WHERE id_sala = NEW.id_sala
              AND data_consulta = NEW.data_consulta
              AND status_consulta <> 'Cancelada'
              AND NEW.hora_inicio < hora_fim
              AND NEW.hora_fim > hora_inicio
              AND (TG_OP = 'INSERT' OR id_consulta <> NEW.id_consulta)
       ) THEN
        RAISE EXCEPTION 'Erro: conflito de horario para a sala';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_consultas_validar
BEFORE INSERT OR UPDATE ON consultas
FOR EACH ROW
EXECUTE FUNCTION validar_consulta();

CREATE OR REPLACE FUNCTION validar_prontuario()
RETURNS TRIGGER AS $$
BEGIN
    IF (SELECT status_consulta FROM consultas WHERE id_consulta = NEW.id_consulta) <> 'Realizada' THEN
        RAISE EXCEPTION 'Erro: prontuario apenas para consulta realizada';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_prontuario_validar
BEFORE INSERT OR UPDATE ON prontuarios
FOR EACH ROW
EXECUTE FUNCTION validar_prontuario();

CREATE OR REPLACE FUNCTION validar_pagamento()
RETURNS TRIGGER AS $$
DECLARE
    total_pagamentos INTEGER;
    valor_consulta INTEGER;
    tipo_consulta VARCHAR(20);
    status_da_consulta VARCHAR(20);
BEGIN
    SELECT status_consulta, valor_consulta_centavos, forma_atendimento
    INTO status_da_consulta, valor_consulta, tipo_consulta
    FROM consultas
    WHERE id_consulta = NEW.id_consulta;

    IF status_da_consulta = 'Cancelada' THEN
        RAISE EXCEPTION 'Erro: nao registrar pagamento para consulta cancelada';
    END IF;

    SELECT COALESCE(SUM(valor_pago_centavos), 0)
    INTO total_pagamentos
    FROM pagamentos
    WHERE id_consulta = NEW.id_consulta
      AND status_pagamento <> 'Cancelado'
      AND (TG_OP = 'INSERT' OR id_pagamento <> NEW.id_pagamento);

    IF total_pagamentos + CASE
        WHEN NEW.status_pagamento <> 'Cancelado' THEN NEW.valor_pago_centavos
        ELSE 0
    END > valor_consulta THEN
        RAISE EXCEPTION 'Erro: soma dos pagamentos maior que o valor da consulta';
    END IF;

    IF NEW.forma_pagamento = 'Convenio' AND tipo_consulta <> 'Convenio' THEN
        RAISE EXCEPTION 'Erro: pagamento por convenio apenas para consulta por convenio';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_pagamento_validar
BEFORE INSERT OR UPDATE ON pagamentos
FOR EACH ROW
EXECUTE FUNCTION validar_pagamento();

