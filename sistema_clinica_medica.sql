-- Consulta: relatório completo de consultas médicas.
SELECT
    c.id_consulta,
    SUBSTR(p.cpf, 1, 3) || '.' ||
    SUBSTR(p.cpf, 4, 3) || '.' ||
    SUBSTR(p.cpf, 7, 3) || '-' ||
    SUBSTR(p.cpf, 10, 2) AS cpf_paciente,
    p.nome AS paciente,
    CASE
        WHEN LENGTH(p.telefone) = 11 THEN
            '(' || SUBSTR(p.telefone, 1, 2) || ') ' ||
            SUBSTR(p.telefone, 3, 5) || '-' ||
            SUBSTR(p.telefone, 8, 4)
        WHEN LENGTH(p.telefone) = 10 THEN
            '(' || SUBSTR(p.telefone, 1, 2) || ') ' ||
            SUBSTR(p.telefone, 3, 4) || '-' ||
            SUBSTR(p.telefone, 7, 4)
        ELSE p.telefone
    END AS telefone_paciente,
    m.nome AS medico,
    'CRM-' || m.estado_crm || '-' || m.crm AS crm_formatado,
    e.nome_especialidade,
    ta.nome_atendimento,
    s.nome_sala,
    c.forma_atendimento,
    COALESCE(cv.nome_convenio, 'Particular') AS convenio,
    TO_CHAR(c.data_consulta, 'DD/MM/YYYY') AS data_consulta,
    TO_CHAR(c.hora_inicio, 'HH24:MI') || ' - ' || TO_CHAR(c.hora_fim, 'HH24:MI') AS horario,
    'R$ ' || REPLACE(TO_CHAR(c.valor_consulta_centavos / 100.0, 'FM999999990.00'), '.', ',') AS valor_consulta,
    c.status_consulta,
    c.observacoes
FROM consultas c
INNER JOIN pacientes p
    ON c.id_paciente = p.id_paciente
INNER JOIN medicos m
    ON c.id_medico = m.id_medico
INNER JOIN especialidades e
    ON c.id_especialidade = e.id_especialidade
INNER JOIN tipos_atendimento ta
    ON c.id_tipo_atendimento = ta.id_tipo_atendimento
    AND c.id_especialidade = ta.id_especialidade
INNER JOIN salas s
    ON c.id_sala = s.id_sala
LEFT JOIN convenios cv
    ON c.id_convenio = cv.id_convenio
ORDER BY c.data_consulta, c.hora_inicio;
