create or replace function public.validar_disciplina_ofertada()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_disciplina text;
  v_disciplina_norm text;
  v_possui_espanhol boolean;
  v_possui_tecnociencia boolean;
begin
  select d.nome, public.f_normalizar_busca(d.nome), e.possui_lingua_espanhola, e.possui_tecnociencia
    into v_disciplina, v_disciplina_norm, v_possui_espanhol, v_possui_tecnociencia
  from public.disciplinas d
  join public.turmas t on t.id = new.turma_id
  join public.escolas e on e.id = t.escola_id
  where d.id = new.disciplina_id;

  if v_disciplina is null then
    raise exception 'Disciplina inexistente para vínculo com a turma.';
  end if;

  if v_disciplina_norm = 'LINGUA ESPANHOLA' and coalesce(v_possui_espanhol, false) = false then
    raise exception 'Disciplina bloqueada: a escola informada não oferta Língua Espanhola.';
  end if;

  if v_disciplina_norm like '%TECNOCIENCIA%' and coalesce(v_possui_tecnociencia, false) = false then
    raise exception 'Disciplina bloqueada: a escola informada não oferta Tecnociência.';
  end if;

  return new;
end;
$$;

create or replace function public.validar_registro_nota_disciplina_da_turma()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_turma_id bigint;
begin
  select turma_id
    into v_turma_id
  from public.matriculas
  where id = new.matricula_id;

  if v_turma_id is null then
    raise exception 'Matrícula inexistente para o registro de nota.';
  end if;

  if not exists (
    select 1
    from public.turma_disciplinas td
    where td.turma_id = v_turma_id
      and td.disciplina_id = new.disciplina_id
  ) then
    raise exception 'Disciplina não vinculada à turma da matrícula.';
  end if;

  return new;
end;
$$;

create or replace function public.validar_matricula_ano_turma()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_ano_turma integer;
begin
  select ano_letivo
    into v_ano_turma
  from public.turmas
  where id = new.turma_id;

  if v_ano_turma is null then
    raise exception 'Turma inexistente para a matrícula.';
  end if;

  if new.ano_letivo <> v_ano_turma then
    raise exception 'Ano letivo da matrícula (%) diferente do ano letivo da turma (%).', new.ano_letivo, v_ano_turma;
  end if;

  return new;
end;
$$;

create or replace function public.validar_observacao_matricula_estudante()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_estudante_id bigint;
begin
  if new.matricula_id is null then
    return new;
  end if;

  select estudante_id
    into v_estudante_id
  from public.matriculas
  where id = new.matricula_id;

  if v_estudante_id is null then
    raise exception 'Matrícula inexistente para a observação.';
  end if;

  if v_estudante_id <> new.estudante_id then
    raise exception 'A observação aponta para estudante diferente do estudante da matrícula.';
  end if;

  return new;
end;
$$;

