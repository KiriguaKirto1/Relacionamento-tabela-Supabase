insert into public.observacoes_estudantis (
  matricula_id,
  estudante_id,
  tipo,
  pagina_original,
  observacao,
  fonte_id_original
)
select
  rn.matricula_id,
  m.estudante_id,
  'Individual'::public.tipo_observacao_enum,
  min(rn.pagina_original) as pagina_original,
  regexp_replace(trim(rn.observacoes_individuais), '\s+', ' ', 'g') as observacao,
  'RN-OBS-' || min(rn.id)::text as fonte_id_original
from public.registros_notas rn
join public.matriculas m on m.id = rn.matricula_id
where rn.observacoes_individuais is not null
  and length(trim(rn.observacoes_individuais)) > 0
  and not exists (
    select 1
    from public.observacoes_estudantis oe
    where oe.estudante_id = m.estudante_id
      and coalesce(oe.matricula_id, -1) = coalesce(rn.matricula_id, -1)
      and oe.tipo = 'Individual'::public.tipo_observacao_enum
      and public.f_normalizar_busca(oe.observacao) = public.f_normalizar_busca(rn.observacoes_individuais)
  )
group by rn.matricula_id, m.estudante_id, regexp_replace(trim(rn.observacoes_individuais), '\s+', ' ', 'g')
on conflict do nothing;

insert into public.observacoes_estudantis (
  matricula_id,
  estudante_id,
  tipo,
  pagina_original,
  observacao,
  fonte_id_original
)
select
  rn.matricula_id,
  m.estudante_id,
  'Conversão'::public.tipo_observacao_enum,
  min(rn.pagina_original) as pagina_original,
  regexp_replace(trim(rn.observacao_conversao), '\s+', ' ', 'g') as observacao,
  'RN-CONV-' || min(rn.id)::text as fonte_id_original
from public.registros_notas rn
join public.matriculas m on m.id = rn.matricula_id
where rn.observacao_conversao is not null
  and length(trim(rn.observacao_conversao)) > 0
  and not exists (
    select 1
    from public.observacoes_estudantis oe
    where oe.estudante_id = m.estudante_id
      and coalesce(oe.matricula_id, -1) = coalesce(rn.matricula_id, -1)
      and oe.tipo = 'Conversão'::public.tipo_observacao_enum
      and public.f_normalizar_busca(oe.observacao) = public.f_normalizar_busca(rn.observacao_conversao)
  )
group by rn.matricula_id, m.estudante_id, regexp_replace(trim(rn.observacao_conversao), '\s+', ' ', 'g')
on conflict do nothing;

delete from public.observacoes_estudantis o
using public.observacoes_estudantis keep
where o.id > keep.id
  and o.estudante_id = keep.estudante_id
  and coalesce(o.matricula_id, -1) = coalesce(keep.matricula_id, -1)
  and o.tipo = keep.tipo
  and public.f_normalizar_busca(o.observacao) = public.f_normalizar_busca(keep.observacao);

update public.registros_notas
set
  observacoes_individuais = null,
  observacao_conversao = null
where observacoes_individuais is not null
   or observacao_conversao is not null;

