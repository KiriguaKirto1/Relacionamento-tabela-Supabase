create or replace function public.validar_disciplina_ofertada()
returns trigger
language plpgsql
as $$
declare
  v_disciplina text;
  v_possui_espanhol boolean;
  v_possui_tecnociencia boolean;
begin
  select d.nome, e.possui_lingua_espanhola, e.possui_tecnociencia
    into v_disciplina, v_possui_espanhol, v_possui_tecnociencia
  from public.turma_disciplinas td
  right join public.disciplinas d on d.id = new.disciplina_id
  join public.turmas t on t.id = new.turma_id
  join public.escolas e on e.id = t.escola_id
  limit 1;

  if v_disciplina = 'LINGUA ESPANHOLA' and coalesce(v_possui_espanhol, false) = false then
    raise exception 'Disciplina bloqueada: a escola informada não oferta Língua Espanhola.';
  end if;

  if v_disciplina ilike '%TECNOCIENCIA%' and coalesce(v_possui_tecnociencia, false) = false then
    raise exception 'Disciplina bloqueada: a escola informada não oferta Tecnociência.';
  end if;

  return new;
end;
$$;

create or replace function public.validar_registro_nota_disciplina_da_turma()
returns trigger
language plpgsql
as $$
declare
  v_turma_id bigint;
begin
  select turma_id
    into v_turma_id
  from public.matriculas
  where id = new.matricula_id;

  if v_turma_id is null then
    raise exception 'Matrícula inexistente para o registro de nota.';
  end if;

  if not exists (
    select 1
    from public.turma_disciplinas td
    where td.turma_id = v_turma_id
      and td.disciplina_id = new.disciplina_id
  ) then
    raise exception 'Disciplina não vinculada à turma da matrícula.';
  end if;

  return new;
end;
$$;

