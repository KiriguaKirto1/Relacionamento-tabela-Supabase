-- 4. FUNÇÕES DE ACESSO POR PERFIL
-- Para produção, os dados sensíveis não devem ficar apenas com
-- policy "authenticated using true".
--
-- Observação: cadastre os usuários na tabela usuarios_perfis.
-- Enquanto não houver perfil para auth.uid(), a API não retorna
-- dados sensíveis para esse usuário.
-- ============================================================

create or replace function public.f_usuario_tem_perfil(
  p_perfis public.perfil_usuario_enum[],
  p_escola_id bigint default null,
  p_turma_id bigint default null,
  p_disciplina_id bigint default null
)
returns boolean
language sql
stable
security definer
set search_path = public, pg_temp
as $$
  select exists (
    select 1
    from public.usuarios_perfis up
    where up.user_id = auth.uid()
      and up.ativo = true
      and up.perfil = any(p_perfis)
      and (
        up.escola_id is null
        or p_escola_id is null
        or up.escola_id = p_escola_id
      )
      and (
        up.turma_id is null
        or p_turma_id is null
        or up.turma_id = p_turma_id
      )
      and (
        up.disciplina_id is null
        or p_disciplina_id is null
        or up.disciplina_id = p_disciplina_id
      )
  );
$$;

