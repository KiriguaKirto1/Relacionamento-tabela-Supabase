create or replace function public.validar_ocorrencia_matricula_estudante()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_estudante_id bigint;
begin
  if new.matricula_id is null then
    return new;
  end if;

  select estudante_id
    into v_estudante_id
  from public.matriculas
  where id = new.matricula_id;

  if v_estudante_id is null then
    raise exception 'Matrícula inexistente para a ocorrência.';
  end if;

  if v_estudante_id <> new.estudante_id then
    raise exception 'A ocorrência aponta para estudante diferente do estudante da matrícula.';
  end if;

  return new;
end;
$$;

drop trigger if exists trg_validar_ocorrencia_matricula_estudante on public.ocorrencias_estudantis;
create trigger trg_validar_ocorrencia_matricula_estudante
before insert or update on public.ocorrencias_estudantis
for each row execute function public.validar_ocorrencia_matricula_estudante();

