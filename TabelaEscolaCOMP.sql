  e.cgm,
  e.nome as estudante,
  count(distinct td.disciplina_id) as disciplinas_esperadas,
  count(distinct rn.disciplina_id) as disciplinas_lancadas,
  count(distinct td.disciplina_id) - count(distinct rn.disciplina_id) as disciplinas_pendentes,
