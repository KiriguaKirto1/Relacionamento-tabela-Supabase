create or replace function public.f_data_br_para_date(valor text, ano_padrao integer default 2026)
returns date
language plpgsql
immutable
as $$
declare
  v text;
begin
  v := trim(coalesce(valor, ''));

  if v ~ '^[0-9]{2}/[0-9]{2}/[0-9]{4}$' then
    return to_date(v, 'DD/MM/YYYY');
  end if;

  if v ~ '^[0-9]{2}/[0-9]{2}$' then
    return to_date(v || '/' || ano_padrao::text, 'DD/MM/YYYY');
  end if;

  return null;
exception
  when others then
    return null;
end;
$$;

create or replace function public.f_primeira_data_observacao(valor text, ano_padrao integer default 2026)
returns date
language plpgsql
immutable
as $$
declare
  v_data text;
begin
  v_data := substring(coalesce(valor, '') from '([0-9]{2}/[0-9]{2}/[0-9]{4})');

  if v_data is null then
    v_data := substring(coalesce(valor, '') from '([0-9]{2}/[0-9]{2})');
  end if;

  return public.f_data_br_para_date(v_data, ano_padrao);
end;
$$;

create or replace function public.f_data_fim_observacao(valor text, ano_padrao integer default 2026)
returns date
language plpgsql
immutable
as $$
declare
  v_data text;
begin
  v_data := substring(coalesce(valor, '') from '[0-9]{2}/[0-9]{2}/[0-9]{4}\s*-\s*([0-9]{2}/[0-9]{2}/[0-9]{4})');

  if v_data is null then
    v_data := substring(coalesce(valor, '') from '[0-9]{2}/[0-9]{2}\s*-\s*([0-9]{2}/[0-9]{2})');
  end if;

  return public.f_data_br_para_date(v_data, ano_padrao);
end;
$$;

create or replace function public.f_classificar_tipo_ocorrencia(valor text)
returns public.tipo_ocorrencia_pedagogica_enum
language plpgsql
immutable
as $$
declare
  v text;
begin
  v := public.f_normalizar_busca(valor);

  if v like '%CHEGOU ATRAS%' or v like '%ATRASAD%' then
    return 'Atraso';
  elsif v like '%NAO FAZ ATIVIDADE%' or v like '%NAO FEZ ATIVIDADE%' or v like '%NAO FEZ AS ATIVIDADES%' then
    return 'Atividade não realizada';
  elsif v like '%CELULAR%' then
    return 'Uso de celular';
  elsif v like '%DORMINDO%' or v like '%DORMIU%' then
    return 'Dormindo em sala';
  elsif v like '%NAO RETORNOU%' or v like '%SAIU%' or v like '%EVADI%' then
    return 'Saída de sala';
  elsif v like '%AFECCOES%' or v like '%DECRETO LEI%' or v like '%ATESTADO%' then
    return 'Atestado/Afecções';
  elsif v like '%ENCAMINHAD%' or v like '%EQUIPE PEDAGOGICA%' or v like '%ORIENTACAO%' then
    return 'Encaminhamento pedagógico';
  elsif length(trim(v)) > 0 then
    return 'Observação pedagógica';
  else
    return 'Outro';
  end if;
end;
$$;

create or replace function public.f_observacao_da_disciplina(
  p_observacao text,
  p_disciplina_id bigint
)
returns text
language plpgsql
stable
as $$
declare
  v_obs text;
  v_alias text;
  v_inicio integer;
  v_fim integer;
  v_outro_inicio integer;
  v_resultado text;
begin
  if p_observacao is null or p_disciplina_id is null then
    return null;
  end if;

  v_obs := public.f_normalizar_busca(p_observacao);

  select da.alias_normalizado
    into v_alias
  from public.disciplinas_alias da
  where da.disciplina_id = p_disciplina_id
    and strpos(v_obs, da.alias_normalizado) > 0
  order by length(da.alias_normalizado) desc
  limit 1;

  if v_alias is null then
    return null;
  end if;

  v_inicio := strpos(v_obs, v_alias);
  if v_inicio <= 0 then
    return null;
  end if;

  select min(posicao)
    into v_outro_inicio
  from (
    select strpos(substr(v_obs, v_inicio + length(v_alias)), da.alias_normalizado) + v_inicio + length(v_alias) - 1 as posicao
    from public.disciplinas_alias da
    where da.disciplina_id <> p_disciplina_id
      and strpos(substr(v_obs, v_inicio + length(v_alias)), da.alias_normalizado) > 0
  ) x;

  v_fim := coalesce(v_outro_inicio, length(v_obs) + 1);
  v_resultado := trim(substr(v_obs, v_inicio + length(v_alias), v_fim - v_inicio - length(v_alias)));

  v_resultado := regexp_replace(v_resultado, '\s+', ' ', 'g');
  v_resultado := regexp_replace(v_resultado, '([0-9]{2}/[0-9]{2}/[0-9]{4}:)', E'\n\\1', 'g');
  v_resultado := regexp_replace(v_resultado, '([0-9]{2}/[0-9]{2}:)', E'\n\\1', 'g');
  v_resultado := trim(v_resultado);

  if length(v_resultado) = 0 then
    return null;
  end if;

  return v_resultado;
end;
$$;

