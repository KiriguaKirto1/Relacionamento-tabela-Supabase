drop view if exists public.vw_estudantes_resumo_seguro cascade;
create view public.vw_estudantes_resumo_seguro
with (security_invoker = true)
as
select
  e.id as estudante_id,
  concat(left(e.cgm, 3), repeat('*', greatest(length(e.cgm) - 5, 0)), right(e.cgm, 2)) as cgm_mascarado,
  e.nome as estudante,
  tu.codigo as turma,
  m.numero_chamada,
  m.situacao,
  tu.ano_letivo
from public.estudantes e
join public.matriculas m on m.estudante_id = e.id
join public.turmas tu on tu.id = m.turma_id;

drop view if exists public.vw_observacoes_qualidade cascade;
create view public.vw_observacoes_qualidade
with (security_invoker = true)
as
select
  oe.id as ocorrencia_id,
  e.cgm,
  e.nome as estudante,
  tu.codigo as turma,
  m.numero_chamada,
  d.nome as disciplina,
  oe.tipo,
  oe.data_ocorrencia,
  oe.data_fim,
  oe.descricao,
  oe.revisado,
  case
    when oe.descricao ilike '%...%' then 'Texto possivelmente cortado'
    when oe.data_ocorrencia is null then 'Sem data identificada'
    when oe.disciplina_id is null and oe.tipo not in ('Atestado/Afecções','Observação pedagógica','Outro') then 'Sem disciplina identificada'
    when oe.revisado = false then 'Pendente de revisão'
    else 'OK'
  end as status_qualidade
from public.ocorrencias_estudantis oe
join public.estudantes e on e.id = oe.estudante_id
left join public.matriculas m on m.id = oe.matricula_id
left join public.turmas tu on tu.id = m.turma_id
left join public.disciplinas d on d.id = oe.disciplina_id;

drop view if exists public.vw_metricas_qualidade_dados cascade;
create view public.vw_metricas_qualidade_dados
with (security_invoker = true)
as
select 'estudantes' as indicador, count(*)::numeric as valor from public.estudantes
union all
select 'matriculas', count(*)::numeric from public.matriculas
union all
select 'registros_notas', count(*)::numeric from public.registros_notas
union all
select 'observacoes_brutas', count(*)::numeric from public.observacoes_estudantis
union all
select 'ocorrencias_padronizadas', count(*)::numeric from public.ocorrencias_estudantis
union all
select 'ocorrencias_pendentes_revisao', count(*)::numeric from public.ocorrencias_estudantis where revisado = false
union all
select 'ocorrencias_texto_cortado', count(*)::numeric from public.ocorrencias_estudantis where descricao ilike '%...%'
union all
select 'boletins_incompletos', count(*)::numeric from public.vw_completude_boletim where status_boletim <> 'Completo'
union all
select 'inconsistencias_avancadas', count(*)::numeric from public.vw_inconsistencias_dados_avancada;

