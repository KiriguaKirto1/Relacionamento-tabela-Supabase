  end as status_boletim
from public.matriculas m
join public.estudantes e on e.id = m.estudante_id
join public.turmas tu on tu.id = m.turma_id
join public.escolas esc on esc.id = tu.escola_id
left join public.turma_disciplinas td on td.turma_id = tu.id
left join public.registros_notas rn on rn.matricula_id = m.id and rn.disciplina_id = td.disciplina_id
