create or replace view public.vw_boletim_estudantes
with (security_invoker = true)
as
select
  e.id as estudante_id,
  e.nome as estudante,
  e.cgm,
  e.data_nascimento,
  cid.nome as cidade,
  esc.nome as escola,
  c.nome as curso,
  se.nome as serie,
  tu.codigo as turma,
  tno.nome as turno,
  m.numero_chamada,
  m.situacao,
  tri.nome as trimestre,
  ia.codigo as indicador,
  d.nome as disciplina,
  d.area_conhecimento,
  rn.faltas,
  rn.valor_total,
  rn.nota_final,
  rn.av1,
  rn.r1,
  rn.av2,
  rn.r2,
  rn.av3,
  rn.r3,
  rn.pagina_original,
  rn.observacoes_individuais,
  rn.observacao_conversao
from public.registros_notas rn
join public.matriculas m on m.id = rn.matricula_id
join public.estudantes e on e.id = m.estudante_id
join public.turmas tu on tu.id = m.turma_id
join public.escolas esc on esc.id = tu.escola_id
join public.cidades cid on cid.id = esc.cidade_id
join public.cursos c on c.id = tu.curso_id
join public.series_escolares se on se.id = tu.serie_id
join public.turnos tno on tno.id = tu.turno_id
join public.disciplinas d on d.id = rn.disciplina_id
join public.trimestres tri on tri.id = rn.trimestre_id
left join public.indicadores_avaliacao ia on ia.id = rn.indicador_id
order by tu.codigo, m.numero_chamada, e.nome, d.nome;

create or replace view public.vw_resumo_turma
with (security_invoker = true)
as
select
  tu.codigo as turma,
  c.nome as curso,
  e.id as estudante_id,
  e.nome as estudante,
  e.cgm,
  m.numero_chamada,
  m.situacao,
  count(rn.id) as total_registros,
  round(avg(rn.nota_final)::numeric, 2) as media_geral,
  sum(rn.faltas) as total_faltas
from public.matriculas m
join public.estudantes e on e.id = m.estudante_id
join public.turmas tu on tu.id = m.turma_id
join public.cursos c on c.id = tu.curso_id
left join public.registros_notas rn on rn.matricula_id = m.id
group by tu.codigo, c.nome, e.id, e.nome, e.cgm, m.numero_chamada, m.situacao
order by tu.codigo, m.numero_chamada, e.nome;

create or replace view public.vw_disciplinas_ofertadas
with (security_invoker = true)
as
select
  esc.nome as escola,
  tu.codigo as turma,
  c.nome as curso,
  d.nome as disciplina,
  d.area_conhecimento
from public.turma_disciplinas td
join public.turmas tu on tu.id = td.turma_id
join public.escolas esc on esc.id = tu.escola_id
join public.cursos c on c.id = tu.curso_id
join public.disciplinas d on d.id = td.disciplina_id
order by esc.nome, tu.codigo, d.nome;

