-- ============================================================
-- TÍTULO: Sistema de Locação de Veículos
-- DESCRIÇÃO: Atividade SQL para controlar clientes, veículos e
-- locações, usando chaves primárias, estrangeiras, validações CHECK
-- e consultas com JOIN.
-- BANCO: Supabase / PostgreSQL
-- ============================================================

DROP TABLE IF EXISTS locacoes;
DROP TABLE IF EXISTS veiculos;
DROP TABLE IF EXISTS clientes;

CREATE TABLE clientes (
    cpf VARCHAR(11) PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    telefone VARCHAR(11),
    email VARCHAR(100),

    CHECK (cpf ~ '^[0-9]{11}$'),
    CHECK (telefone IS NULL OR telefone ~ '^[0-9]{10,11}$'),
    CHECK (email IS NULL OR email LIKE '%@%')
);

CREATE TABLE veiculos (
    id_veiculo BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    marca VARCHAR(50) NOT NULL,
    modelo VARCHAR(50) NOT NULL,
    ano INTEGER NOT NULL CHECK (ano >= 1900),
    placa VARCHAR(10) NOT NULL UNIQUE,
    valor_diaria NUMERIC(10,2) NOT NULL CHECK (valor_diaria >= 0),
    status_veiculo VARCHAR(20) NOT NULL CHECK (
        status_veiculo IN ('Disponível', 'Locado', 'Manutenção')
    ),

    CHECK (placa ~ '^[A-Z]{3}[0-9][A-Z0-9][0-9]{2}$')
);

CREATE TABLE locacoes (
    id_locacao BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cpf_cliente VARCHAR(11) NOT NULL,
    id_veiculo BIGINT NOT NULL,
    data_inicio DATE NOT NULL,
    data_fim DATE NOT NULL,
    valor_total NUMERIC(10,2) NOT NULL CHECK (valor_total >= 0),
    status_locacao VARCHAR(20) NOT NULL CHECK (
        status_locacao IN ('Ativa', 'Finalizada', 'Cancelada')
    ),

    FOREIGN KEY (cpf_cliente) REFERENCES clientes(cpf),
    FOREIGN KEY (id_veiculo) REFERENCES veiculos(id_veiculo),

    CHECK (data_fim >= data_inicio)
);

