-- Consulta 1: clientes com CPF formatado.
SELECT
    SUBSTR(cpf, 1, 3) || '.' ||
    SUBSTR(cpf, 4, 3) || '.' ||
    SUBSTR(cpf, 7, 3) || '-' ||
    SUBSTR(cpf, 10, 2) AS cpf_formatado,
    nome,
    telefone,
    email
FROM clientes;

-- Consulta 2: veículos com valor da diária formatado.
SELECT
    id_veiculo,
    marca,
    modelo,
    ano,
    placa,
    'R$ ' || REPLACE(TO_CHAR(valor_diaria, 'FM999999990.00'), '.', ',') AS valor_diaria_formatado,
    status_veiculo
FROM veiculos;

-- Consulta 3: locações com datas e valor formatados.
SELECT
    id_locacao,
    SUBSTR(cpf_cliente, 1, 3) || '.' ||
    SUBSTR(cpf_cliente, 4, 3) || '.' ||
    SUBSTR(cpf_cliente, 7, 3) || '-' ||
    SUBSTR(cpf_cliente, 10, 2) AS cpf_cliente_formatado,
    id_veiculo,
    TO_CHAR(data_inicio, 'DD/MM/YYYY') AS data_inicio_formatada,
    TO_CHAR(data_fim, 'DD/MM/YYYY') AS data_fim_formatada,
    'R$ ' || REPLACE(TO_CHAR(valor_total, 'FM999999990.00'), '.', ',') AS valor_total_formatado,
    status_locacao
FROM locacoes;

-- Consulta 4: relatório completo de locações.
SELECT
    locacoes.id_locacao,
    SUBSTR(clientes.cpf, 1, 3) || '.' ||
    SUBSTR(clientes.cpf, 4, 3) || '.' ||
    SUBSTR(clientes.cpf, 7, 3) || '-' ||
    SUBSTR(clientes.cpf, 10, 2) AS cpf_formatado,
    clientes.nome,
    clientes.telefone,
    veiculos.marca,
    veiculos.modelo,
    veiculos.placa,
    TO_CHAR(locacoes.data_inicio, 'DD/MM/YYYY') AS data_inicio_formatada,
    TO_CHAR(locacoes.data_fim, 'DD/MM/YYYY') AS data_fim_formatada,
    'R$ ' || REPLACE(TO_CHAR(locacoes.valor_total, 'FM999999990.00'), '.', ',') AS valor_total_formatado,
    locacoes.status_locacao
FROM locacoes
INNER JOIN clientes
    ON locacoes.cpf_cliente = clientes.cpf
INNER JOIN veiculos
    ON locacoes.id_veiculo = veiculos.id_veiculo;
