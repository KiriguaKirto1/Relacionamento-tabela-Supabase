-- 5. VIEWS SEGURAS
-- ============================================================

create view public.vw_boletim_alunos
with (security_invoker = true)
as
select
  e.nome as escola,
  e.cidade,
  e.estado,
  t.nome as turma,
  t.serie,
  t.turno,
  a.cgm,
  a.nome as aluno,
  d.nome as disciplina,
  n.trimestre,
  n.nota,
  n.faltas
from public.notas n
join public.matriculas m on m.id = n.matricula_id
join public.alunos a on a.cgm = m.aluno_cgm
join public.turmas t on t.id = m.turma_id
join public.escolas e on e.id = t.escola_id
join public.disciplinas d on d.id = n.disciplina_id;

create view public.vw_media_alunos
with (security_invoker = true)
as
select
  e.nome as escola,
  t.nome as turma,
  a.cgm,
  a.nome as aluno,
  round(avg(n.nota), 2) as media_geral,
  sum(n.faltas) as total_faltas,
  case
    when avg(n.nota) >= 7 then 'Aprovado'
    when avg(n.nota) >= 6 then 'Recuperação'
    else 'Reprovado'
  end as situacao
from public.notas n
join public.matriculas m on m.id = n.matricula_id
join public.alunos a on a.cgm = m.aluno_cgm
join public.turmas t on t.id = m.turma_id
join public.escolas e on e.id = t.escola_id
group by e.nome, t.nome, a.cgm, a.nome;

create view public.vw_resumo_escolas
with (security_invoker = true)
as
select
  e.nome as escola,
  e.cidade,
  e.estado,
  count(distinct t.id) as total_turmas,
  count(distinct a.cgm) as total_alunos,
  round(avg(n.nota), 2) as media_geral_escola,
  sum(n.faltas) as total_faltas
from public.escolas e
join public.turmas t on t.escola_id = e.id
join public.matriculas m on m.turma_id = t.id
join public.alunos a on a.cgm = m.aluno_cgm
join public.notas n on n.matricula_id = m.id
group by e.nome, e.cidade, e.estado;

create view public.vw_resumo_turmas
with (security_invoker = true)
as
select
  e.nome as escola,
  t.nome as turma,
  t.serie,
  t.turno,
  count(distinct a.cgm) as total_alunos,
  round(avg(n.nota), 2) as media_turma,
  sum(n.faltas) as total_faltas
from public.turmas t
join public.escolas e on e.id = t.escola_id
join public.matriculas m on m.turma_id = t.id
join public.alunos a on a.cgm = m.aluno_cgm
join public.notas n on n.matricula_id = m.id
group by e.nome, t.nome, t.serie, t.turno;

create view public.vw_resumo_disciplinas
with (security_invoker = true)
as
select
  d.nome as disciplina,
  count(n.id) as total_notas,
  round(avg(n.nota), 2) as media_disciplina,
  sum(n.faltas) as total_faltas
from public.disciplinas d
join public.notas n on n.disciplina_id = d.id
group by d.nome;

