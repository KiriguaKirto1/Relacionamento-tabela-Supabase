-- ============================================================
-- 2. TABELAS PRINCIPAIS
-- ============================================================

-- Uma escola pode ter várias turmas
create table public.escolas (
  id bigint generated by default as identity primary key,
  nome text not null,
  cidade text not null,
  estado char(2) not null
);

-- Cada turma pertence a uma escola
create table public.turmas (
  id bigint generated by default as identity primary key,
  escola_id bigint not null references public.escolas(id) on delete cascade,
  nome text not null,
  serie text not null,
  turno text not null,
  ano_letivo integer not null
);

-- O CGM é a chave primária do aluno
-- Como o CGM é único, ele identifica diretamente cada estudante
create table public.alunos (
  cgm text primary key,
  nome text not null,
  data_nascimento date,

  constraint chk_cgm_numerico check (cgm ~ '^[0-9]+$')
);

-- Cadastro simples das disciplinas
create table public.disciplinas (
  id bigint generated by default as identity primary key,
  nome text not null unique
);

-- Liga alunos às turmas
create table public.matriculas (
  id bigint generated by default as identity primary key,
  aluno_cgm text not null references public.alunos(cgm) on delete cascade,
  turma_id bigint not null references public.turmas(id) on delete cascade,
  numero_chamada integer not null,
  situacao text not null default 'Matriculado',

  constraint uq_aluno_turma unique (aluno_cgm, turma_id)
);

-- Registra as notas dos alunos
create table public.notas (
  id bigint generated by default as identity primary key,
  matricula_id bigint not null references public.matriculas(id) on delete cascade,
  disciplina_id bigint not null references public.disciplinas(id) on delete cascade,
  trimestre text not null,
  nota numeric(4,2) not null,
  faltas integer not null default 0,

  constraint chk_nota_valida check (nota >= 0 and nota <= 10),
  constraint chk_faltas_validas check (faltas >= 0),
  constraint uq_nota_unica unique (matricula_id, disciplina_id, trimestre)
);

